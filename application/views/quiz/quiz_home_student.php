<script type="text/javascript">



function get_quizzes(pageno, status) {

	$.ajax({
		url: '<?php echo base_url();?>onlinequiz/get_quiz_content_student/' + pageno + '/' + status,
	}).done(function (html) {
		$('.quiz-content').html(html);
	});
}

$(document).ready(function() {
	var pageno = 1;
	var status = "outstanding";
	get_quizzes(pageno, status);
	
	$('a[data-toggle="tab"]').on('shown.bs.tab', function (e) {
		var pageno = 1;
		var status = $(e.target).data("lang");
		get_quizzes(pageno, status);
	});
	
	$('body').on('click','.page-link a',function(e){
		e.preventDefault();
		pageno = $(this).attr('data-ci-pagination-page');
		get_quizzes(pageno, 'outstanding');
	});
	
	$('#showHistoricalAttempts').on('click', function(e){
		e.preventDefault();
		if ($(this).text() == "Show Attempt History") {
			$(this).closest("tr").nextAll('.attemptHistory').first().show("slow");
			$(this).text("Hide Attempt History");
		} else 
		if ($(this).text() == "Hide Attempt History") {
			$(this).closest("tr").nextAll('.attemptHistory').first().hide("slow");
			$(this).text("Show Attempt History");
		}
	});
});
</script>

